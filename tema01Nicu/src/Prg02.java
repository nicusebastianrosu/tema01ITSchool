public class Prg02 {
    public static void main(String[] args) {
        int n1 = 10;
        int n2 = 3;
        System.out.println("Fie numerele intregi:");
        System.out.println("n1 = " + n1);
        System.out.println("n2 = " + n2);
        System.out.println("n1 + n2 = " + (n1 + n2));
        System.out.println("n1 x n2 = " + (n1 * n2));
        System.out.println("n1 - n2 = " + (n1 - n2));
        System.out.println("n1 / n2 = " + (n1 / n2));
        System.out.println("n1 % n2 = " + (n1 % n2));

    }
}
