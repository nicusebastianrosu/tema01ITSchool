public class Prg05 {
    public static void main(String[] args) {
        double latimea = 5.0;
        double lungimea = 10.0;

        double perimetrul = 2 * (latimea + lungimea);
        double aria = latimea * lungimea;

        System.out.println("Latimea dreptunghiului: " + latimea);
        System.out.println("Lungimea dreptunghiului: " + lungimea);
        System.out.println("Perimetrul dreptunghiului: " + perimetrul);
        System.out.println("Aria dreptunghiului: " + aria);
    }
}
